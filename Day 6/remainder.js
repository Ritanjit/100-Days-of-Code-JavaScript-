/* Remainder Operator --> The seeker of Odd and Even Numbers */

var bigNumber = 13484;

let smallNumber = 45;

const REMAINDER = bigNumber % smallNumber;

console.log("\nFinding the remainder of a large number...");
console.log("Your remainder is -->" + REMAINDER);

// IMP TIP : The remainder operator is not same as the modulus operator in Javascript.

// On finding the modulus of negative numbers, the remainder operator will give faulty positive value.